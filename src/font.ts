export function drawText(text: string, x: number, y: number, color: string, scale = 2) {
  bx.fillStyle = color;
  let cx = Math.round(x);
  const py = Math.round(y);
  for (const ch of text.toUpperCase()) {
    const glyph = PX[ch];
    if (glyph) {
      for (let row = 0; row < 6; row++) {
        const bits = glyph[row];
        for (let col = 0; col < 4; col++) {
          if (bits & (1 << (3 - col))) {
            bx.fillRect(cx + col * scale, py + row * scale, scale, scale);
          }
        }
      }
    }
    cx += 5 * scale;
  }
}

export function textWidth(text: string, scale = 2): number {
  return text.length * 5 * scale;
}

export function drawIcon(icon: number[][], x: number, y: number, color: string) {
  bx.fillStyle = color;
  for (let row = 0; row < 5; row++) {
    for (let col = 0; col < 5; col++) {
      if (icon[row] && icon[row][col]) {
        bx.fillRect(x + col, y + row, 1, 1);
      }
    }
  }
}

export function formatTime(frames: number): string {
  const totalSec = Math.floor(frames / FPS);
  const min = Math.floor(totalSec / 60);
  const sec = totalSec % 60;
  return (min < 10 ? '0' : '') + min + ':' + (sec < 10 ? '0' : '') + sec;
}
